<p>With the boom in e-commerce, shopping carts are the heart of the website. Often a shopping cart is either a small area generally found up in the header of the website that keeps a running tally (often called a Quick View cart) or a dedicated page where users can see and review their items before proceeding with checkout<sup>1</sup>. 
</p>
<p>There are many ways you can integrate a shopping cart into your website, build it from scratch or using a third party(plugins). If you are creating a website using WordPress, you can use a plugin that will allow the integration of payment processing from Paypal<sup>2</sup>. 
</p>
<p>SWhen creating a shopping cart, security should be a main concern because user’s data can be breached. You can use an SSL certificate to secure your shopping carts, require advanced verification methods (Credit Card CVC, Address, Expiration Date, etc.), and create a site policy to educate your customers and protect yourself<sup>3</sup>. 
</p>
<p>Shopping carts/payment method can improve the revenue your website can bring in but you have to make sure it is secured and policy in place to help protect you. Otherwise, it’s best to integrate a 3rd party shopping cart software in your website. 
</p>
